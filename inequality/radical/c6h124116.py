from sympy import *

# https://artofproblemsolving.com/community/c6h124116

def main():
    x, y, z, t = symbols('x, y, z, t', positive = True)
    # a**2 - b**2 <= 0 && a + b >= 0 => a - b <= 0
    f = (sqrt(578*x**2 + 1143*y*z) + sqrt(578*y**2 + 1143*z*x))**2 - (253*sqrt(527)*(x + y + z)/140 - sqrt(578*z**2 + 1143*x*y))**2
    # to prove: sqrt(578*x**2 + 1143*y*z) + sqrt(578*y**2 + 1143*z*x) + |253*sqrt(527)*(x + y + z)/140 - sqrt(578*z**2 + 1143*x*y)| >= 0 ?
    print('f =', factor(f))
    # should prove negative:
    g = (11328800*x**2 - 22402800*x*y + 22402800*x*z + 11328800*y**2 + 22402800*y*z - 11328800*z**2 - 33732743)**2 - (39200*sqrt(578*x**2 + 1143*y*z)*sqrt(1143*x*z + 578*y**2) + 70840*sqrt(527)*sqrt(1143*x*y + 578*z**2))**2
    # to prove: 11328800*x**2 - 22402800*x*y + 22402800*x*z + 11328800*y**2 + 22402800*y*z - 11328800*z**2 - 33732743 + 39200*sqrt(578*x**2 + 1143*y*z)*sqrt(1143*x*z + 578*y**2) + 70840*sqrt(527)*sqrt(1143*x*y + 578*z**2) >= 0 ?
    print('g =', factor(g))
    # should prove negative:
    h = (128341709440000*x**4 - 507593681280000*x**3*y - 507593681280000*x**3*z + 245202028960000*x**2*y**2 - 496177214400000*x**2*y*z + 245202028960000*x**2*z**2 - 764302997796800*x**2 - 507593681280000*x*y**3 - 496177214400000*x*y**2*z - 496177214400000*x*y*z**2 - 1511415789760800*x*y - 507593681280000*x*z**3 - 1511415789760800*x*z + 128341709440000*y**4 - 507593681280000*y**3*z + 245202028960000*y**2*z**2 - 764302997796800*y**2 - 507593681280000*y*z**3 - 1511415789760800*y*z + 128341709440000*z**4 - 764302997796800*z**2 + 1137897950304049)**2 - (5553856000*sqrt(527)*sqrt(578*x**2 + 1143*y*z)*sqrt(1143*x*y + 578*z**2)*sqrt(1143*x*z + 578*y**2))**2
    # to prove: 128341709440000*x**4 - 507593681280000*x**3*y - 507593681280000*x**3*z + 245202028960000*x**2*y**2 - 496177214400000*x**2*y*z + 245202028960000*x**2*z**2 - 764302997796800*x**2 - 507593681280000*x*y**3 - 496177214400000*x*y**2*z - 496177214400000*x*y*z**2 - 1511415789760800*x*y - 507593681280000*x*z**3 - 1511415789760800*x*z + 128341709440000*y**4 - 507593681280000*y**3*z + 245202028960000*y**2*z**2 - 764302997796800*y**2 - 507593681280000*y*z**3 - 1511415789760800*y*z + 128341709440000*z**4 - 764302997796800*z**2 + 1137897950304049 + 5553856000*sqrt(527)*sqrt(578*x**2 + 1143*y*z)*sqrt(1143*x*y + 578*z**2)*sqrt(1143*x*z + 578*y**2) ?
    print('h =', factor(h))
    print('Is h cyclic?', h.subs(z, t).subs(y, z).subs(x, y).subs(t, x) == h)
    print('Is h symmetric?', h.subs(y, t).subs(x, y).subs(t, x) == h)
    s3 = Integer(1)/3
    print('h(1/3) =', h.subs(x, s3).subs(y, s3).subs(z, s3))
    u, v = symbols('u, v', positive = True)
    # x <= y <= 1/3 <= z
    print('f(xy3z) =', factor(h.subs(z, 1 - x - y).subs(y, s3/(1 + u)).subs(x, s3/(1 + u + v))))
    # x <= 1/3 <= y <= (1 - x)/2, z = 1 - x - y >= y
    print('f(x3yz) =', factor(h.subs(z, 1 - x - y).subs(y, s3 + ((1 - x)/2 - s3)/(1 + v)).subs(x, s3/(1 + u))))

if __name__ == '__main__':
    main()